n = int(input())

if n == 1:
    print('*')
else:
    for i in range(0,((n-1)*4+1)//2 + 1):
        if i % 2 != 0:
            print('* '*((i//2)+1) + ' '*(((n-1)*4+1) - 4*((i//2)+1)) + ' *'*((i//2)+1))
        else:
            print('* '*(i//2) + '*'*(((n-1)*4+1) - 4*((i//2))) + ' *'*(i//2))
            
    for i in range(((n-1)*4+1)//2 - 1, -1, -1):
        if i % 2 != 0:
            print('* '*((i//2)+1) + ' '*(((n-1)*4+1) - 4*((i//2)+1)) + ' *'*((i//2)+1))
        else:
            print('* '*(i//2) + '*'*(((n-1)*4+1) - 4*((i//2))) + ' *'*(i//2))